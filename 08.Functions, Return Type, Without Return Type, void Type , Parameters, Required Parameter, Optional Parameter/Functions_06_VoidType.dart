//|||||||Functions =====  VOID TYPE|||||

// A function with a void return type is a function
// that performs an action but doesn't return any data to the calling code.

//Void functions do not return a value.
//They commonly perform actions such as printing, modifying variables, and interacting with systems.
//They can contain control flow, error handling, and event handling logic.
//Void functions are used for initialization, cleanup, and event-driven programming.
//Their purpose isn't limited to actions; they can encompass a wide range of functionalities based on program requirements.

void greet() {
  print("Hi There");
}

void main() {
  //call greet function in main function
  greet(); //This will shows what its action
  //Output >> Hi There
}
